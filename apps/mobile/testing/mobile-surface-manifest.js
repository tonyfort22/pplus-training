export function getMobileSurfaceById(surfaceId) {
  return MOBILE_SURFACE_MANIFEST.find((surface) => surface.id === surfaceId) || null;
}

export function getMobileSurfaceCoverageSummary() {
  const byLayer = Object.values(MOBILE_TEST_LAYERS).map((layer) => {
    const surfaces = MOBILE_SURFACE_MANIFEST.filter((surface) => surface.requiredLayers.includes(layer));
    const testFiles = Array.from(new Set(surfaces.flatMap((surface) => surface.existingTestFiles))).sort();

    return Object.freeze({
      layer,
      surfaceCount: surfaces.length,
      surfaceIds: Object.freeze(surfaces.map((surface) => surface.id)),
      testFileCount: testFiles.length,
      testFiles: Object.freeze(testFiles),
    });
  });

  return Object.freeze({
    surfaceCount: MOBILE_SURFACE_MANIFEST.length,
    layerCount: Object.keys(MOBILE_TEST_LAYERS).length,
    sourceTestGroupCount: Object.keys(MOBILE_SOURCE_TEST_GROUPS).length,
    fullSourceTestFileCount: FULL_MOBILE_SOURCE_TESTS.testFiles.length,
    byLayer: Object.freeze(byLayer),
  });
}
