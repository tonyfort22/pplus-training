  assert.match(calendarSource, /Open event editor/)
  assert.match(calendarSource, /function StaticEventCard[\s\S]*rounded-\[16px\] border px-3 py-3/)
  assert.match(calendarSource, /function StaticEventCard[\s\S]*line-clamp-2/)
  assert.match(calendarSource, /function StaticEventCard[\s\S]*text-\[10px\] font-semibold uppercase tracking-\[0\.16em\]/)
  assert.match(calendarSource, /getEventToneClasses/)
  assert.match(calendarSource, /Ends \{event\.endHour\}/)
  assert.match(calendarSource, /rounded-\[18px\] border px-4 py-4 text-left shadow-\[0_10px_24px_rgba\(0,0,0,0\.18\)\]/)
  assert.doesNotMatch(calendarSource, /Move earlier/)
  assert.doesNotMatch(calendarSource, /Move later/)
  assert.doesNotMatch(calendarSource, /Drag workout/)

  assert.match(calendarSource, /const \[isMonthOverflowDialogOpen, setIsMonthOverflowDialogOpen\] = useState\(false\)/)
  assert.match(calendarSource, /const \[monthOverflowDate, setMonthOverflowDate\] = useState\(null\)/)
  assert.match(calendarSource, /const \[dayPlaylistEventIds, setDayPlaylistEventIds\] = useState\(\[\]\)/)
  assert.match(calendarSource, /const \[dayPlaylistIndex, setDayPlaylistIndex\] = useState\(0\)/)
  assert.match(calendarSource, /useSearchParams/)
  assert.match(calendarSource, /debugDenseDay/)
  assert.match(calendarSource, /const DEBUG_DENSE_DAY_EVENTS = \[/)
  assert.match(calendarSource, /Preview mode/)
  assert.match(calendarSource, /Debug dense day/)
  assert.match(calendarSource, /forceDenseDayPreview \? \(/)
  assert.match(calendarSource, /function renderMonthGrid[\s\S]*events\.slice\(0, 2\)/)
  assert.match(calendarSource, /function renderMonthGrid[\s\S]*events\.length > 2/)
  assert.match(calendarSource, /function renderMonthGrid[\s\S]*Open workout overflow for day/)
