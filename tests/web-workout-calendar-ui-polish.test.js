  assert.match(calendarSource, /playlistKey = `\$\{event\.startHour \?\? '06:00'\}-\$\{event\.title\.trim\(\)\.toLowerCase\(\)\}`/)
  assert.match(calendarSource, /const eventIds = buildDayPlaylistEventIds\(events\)/)
  assert.match(calendarSource, /setDayPlaylistEventIds\(eventIds\)/)
  assert.match(calendarSource, /setDayPlaylistIndex\(0\)/)
  assert.match(calendarSource, /openEventDialog\(firstEvent\.id, \{ dayPlaylistEventIds: eventIds, dayPlaylistIndex: 0, dayPlaylistRawCount: events\.length \}\)/)
  assert.match(calendarSource, /const activeDayPlaylistEvents = useMemo/)
  assert.match(calendarSource, /const unresolvedDayPlaylistEventIds = useMemo/)
  assert.match(calendarSource, /const activeDayPlaylistDedupedCount = activeDayPlaylistEvents\.length/)
  assert.match(calendarSource, /const activeDayPlaylistRawCount = dayPlaylistRawCount/)
  assert.match(calendarSource, /const isDayPlaylistActive = activeDayPlaylistEvents\.length > 1/)
  assert.match(calendarSource, /function goToDayPlaylistEvent\(nextIndex\)/)
