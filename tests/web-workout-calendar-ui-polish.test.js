  assert.match(calendarSource, /max-w-\[calc\(100%-1rem\)\][\s\S]*rounded-t-\[28px\][\s\S]*rounded-b-\[18px\][\s\S]*p-5/)
  assert.match(calendarSource, /data-\[state=open\]:slide-in-from-bottom-\[18%\]/)
  assert.match(calendarSource, /data-\[state=closed\]:slide-out-to-bottom-\[18%\]/)
  assert.match(calendarSource, /sm:max-w-\[420px\]/)
  assert.match(calendarSource, /sm:rounded-\[28px\]/)
  assert.match(calendarSource, /sm:bottom-auto[\s\S]*sm:top-\[50%\][\s\S]*sm:-translate-y-1\/2/)
